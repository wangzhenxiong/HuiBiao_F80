    public void createNotification(String title, String message, int icon, String imageUrl, Intent intent,int priority) {
        // notification icon
        //final int icon = R.drawable.ic_luncher;

        if (android.os.Build.VERSION.SDK_INT > 9) {
            StrictMode.ThreadPolicy policy = new StrictMode.ThreadPolicy.Builder().permitAll().build();
            StrictMode.setThreadPolicy(policy);
        }
        mBuilder = new NotificationCompat.Builder(mContext).setPriority(priority);
        intent.setFlags(Intent.FLAG_ACTIVITY_CLEAR_TOP | Intent.FLAG_ACTIVITY_SINGLE_TOP);
        final PendingIntent resultPendingIntent =
                PendingIntent.getActivity(
                        mContext,
                        0,
                        intent,
                        PendingIntent.FLAG_CANCEL_CURRENT
                );

        if (!TextUtils.isEmpty(imageUrl)) {

            if (imageUrl != null && imageUrl.length() > 4 && Patterns.WEB_URL.matcher(imageUrl).matches()) {

                Bitmap bitmap = getBitmapFromURL(imageUrl);

                if (bitmap != null) {
                    showBigNotification(bitmap, mBuilder, icon, title, message, resultPendingIntent);
                } else {
                    showSmallNotification(mBuilder, icon, title, message, resultPendingIntent);
                }
            }
        } else {
            showSmallNotification(mBuilder, icon, title, message, resultPendingIntent);

        }
    }

